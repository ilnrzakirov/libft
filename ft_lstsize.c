#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	count;

	count = 0;
	if (!lst)
		return (0);
	else
	{
		while (lst != NULL)
		{
			count ++;
			lst = lst -> next;
		}
	}
	return (count);
}
